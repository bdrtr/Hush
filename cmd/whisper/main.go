func main() {
	var filename string
	flag.StringVar(&filename, "file", "", "Go file to parse")
	flag.Parse()

	if filename == "" {
		log.Fatal("Lütfen parse edilecek dosyayı -file parametresi ile belirtin.")
	}

	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, filename, nil, parser.ParseComments)
	if err != nil {
		log.Fatal(err)
	}

	packageName := node.Name.Name
	var models []Model

	ast.Inspect(node, func(n ast.Node) bool {
		typeDecl, ok := n.(*ast.TypeSpec)
		if !ok {
			return true
		}

		structType, ok := typeDecl.Type.(*ast.StructType)
		if !ok {
			return true
		}

		modelName := typeDecl.Name.Name
		model := Model{
			Name:      modelName,
			TableName: strings.ToLower(modelName) + "s",
		}

		for _, field := range structType.Fields.List {
			if len(field.Names) == 0 {
				continue
			}
			fieldName := field.Names[0].Name
			
			fieldType := ""
			switch t := field.Type.(type) {
			case *ast.Ident:
				fieldType = t.Name
			case *ast.SelectorExpr:
				if ident, ok := t.X.(*ast.Ident); ok {
					fieldType = ident.Name + "." + t.Sel.Name
				}
			case *ast.StarExpr:
				if ident, ok := t.X.(*ast.Ident); ok {
					fieldType = "*" + ident.Name
				}
			}

			dbName := strings.ToLower(fieldName)
			isPk := false
			isIgnored := false
			
			if field.Tag != nil {
				tagValue := strings.Trim(field.Tag.Value, "`")
				tag := reflect.StructTag(tagValue)
				
				if dbTag, ok := tag.Lookup("db"); ok {
					if dbTag == "-" {
						isIgnored = true
					} else {
						parts := strings.Split(dbTag, ",")
						if parts[0] != "" {
							dbName = parts[0]
						}
						for _, part := range parts[1:] {
							if part == "primary_key" {
								isPk = true
							}
						}
					}
				}

				if whisperTag, ok := tag.Lookup("whisper"); ok && isIgnored {
					parts := strings.Split(whisperTag, ",")
					relTypePart := strings.Split(parts[0], ":")
					
					if len(relTypePart) == 2 {
						relType := relTypePart[0]
						targetModel := relTypePart[1]
						targetTable := strings.ToLower(targetModel) + "s"
						
						fk := ""
						if len(parts) > 1 && strings.HasPrefix(parts[1], "fk:") {
							fk = strings.TrimPrefix(parts[1], "fk:")
						}
						
						targetFkName := toPascalCase(fk)

						model.Relations = append(model.Relations, Relation{
							FieldName:    fieldName,
							TargetModel:  targetModel,
							TargetTable:  targetTable,
							ForeignKey:   fk,
							TargetFKName: targetFkName,
							RelType:      relType,
						})
					}
				}
			}

			if isIgnored {
				continue
			}

			if isPk || (len(model.Fields) == 0 && strings.ToUpper(fieldName) == "ID") {
				isPk = true
				model.PrimaryKeyColumn = dbName
				model.PrimaryKeyName = fieldName
				model.PrimaryKeyType = fieldType
			}

			model.Fields = append(model.Fields, Field{
				Name:         fieldName,
				Type:         fieldType,
				DBName:       dbName,
				IsPrimaryKey: isPk,
			})
		}
		
		var insCols, insArgs, updCols, updArgs, upsertCols []string
		var createCols []string
		updPlaceholderIndex := 1

		for _, f := range model.Fields {
			insCols = append(insCols, f.DBName)
			insArgs = append(insArgs, "m."+f.Name)
			
			if !f.IsPrimaryKey {
				updCols = append(updCols, fmt.Sprintf("%s = $%d", f.DBName, updPlaceholderIndex))
				updArgs = append(updArgs, "m."+f.Name)
				updPlaceholderIndex++

				// Upsert için EXCLUDED keyword'ünü kullanarak placeholder sorununu ortadan kaldır
				upsertCols = append(upsertCols, fmt.Sprintf("%s = EXCLUDED.%s", f.DBName, f.DBName))
			}

			sqlType := "VARCHAR(255)"
			switch f.Type {
			case "int", "int64", "int32":
				if f.IsPrimaryKey {
					sqlType = "SERIAL PRIMARY KEY"
				} else {
					sqlType = "INT"
				}
			case "string":
				sqlType = "VARCHAR(255)"
			case "bool":
				sqlType = "BOOLEAN"
			case "float64", "float32":
				sqlType = "DOUBLE PRECISION"
			case "time.Time", "*time.Time":
				sqlType = "TIMESTAMP"
			}

			if f.IsPrimaryKey && !strings.Contains(sqlType, "PRIMARY KEY") {
				sqlType += " PRIMARY KEY"
			}

			createCols = append(createCols, fmt.Sprintf("%s %s", f.DBName, sqlType))
		}

		var insPlaceholders []string
		var copyCols []string
		for i, col := range insCols {
			insPlaceholders = append(insPlaceholders, fmt.Sprintf("$%d", i+1))
			copyCols = append(copyCols, fmt.Sprintf(`"%s"`, col))
		}

		model.InsertColumns = strings.Join(insCols, ", ")
		model.InsertPlaceholders = strings.Join(insPlaceholders, ", ")
		model.InsertArgs = strings.Join(insArgs, ", ")
		
		model.CopyFromColumns = strings.Join(copyCols, ", ")

		model.UpdateColumns = strings.Join(updCols, ", ")
		model.UpdateArgs = strings.Join(updArgs, ", ")
		model.UpsertColumns = strings.Join(upsertCols, ", ")
		model.UpdatePrimaryKeyPlaceholder = fmt.Sprintf("$%d", updPlaceholderIndex)

		model.CreateTableSQL = fmt.Sprintf("CREATE TABLE IF NOT EXISTS %s (\n\t\t%s\n\t)", model.TableName, strings.Join(createCols, ",\n\t\t"))

		models = append(models, model)
		return false
	})

	tmpl, err := template.New("whisper").Parse(whisperTemplate)
	if err != nil {
		log.Fatalf("Template parse hatası: %v", err)
	}

	var buf bytes.Buffer
	data := struct {
		PackageName string
		Models      []Model
	}{
		PackageName: packageName,
		Models:      models,
	}

	if err := tmpl.Execute(&buf, data); err != nil {
		log.Fatalf("Template execute hatası: %v", err)
	}

	formattedSource, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("Format hatası: %v\n\n%s", err, buf.String())
	}

	outFilename := strings.TrimSuffix(filename, ".go") + "_whisper.go"
	err = os.WriteFile(outFilename, formattedSource, 0644)
	if err != nil {
		log.Fatalf("Dosya yazma hatası: %v", err)
	}

	fmt.Printf("Whisper ORM DataLoader/Batch metodu eklendi!\n")
}
