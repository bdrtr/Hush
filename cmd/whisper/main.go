type Field struct {
	Name         string
	Type         string
	DBName       string
	IsPrimaryKey bool
}

type Relation struct {
	FieldName    string
	TargetModel  string
	ForeignKey   string
	TargetTable  string
	TargetFKName string
	RelType      string
}

type Model struct {
	Name                        string
	TableName                   string
	Fields                      []Field
	Relations                   []Relation
	InsertColumns               string
	InsertPlaceholders          string
	InsertArgs                  string
	UpdateColumns               string
	UpdateArgs                  string
	UpsertColumns               string
	PrimaryKeyColumn            string
	PrimaryKeyName              string
	PrimaryKeyType              string
	UpdatePrimaryKeyPlaceholder string
	CreateTableSQL              string
	CopyFromColumns             string
}

// toPascalCase converts snake_case to PascalCase (e.g. user_id -> UserID)
func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if strings.ToLower(p) == "id" {
			parts[i] = "ID"
		} else if len(p) > 0 {
			parts[i] = strings.ToUpper(p[:1]) + strings.ToLower(p[1:])
		}
	}
	return strings.Join(parts, "")
}

